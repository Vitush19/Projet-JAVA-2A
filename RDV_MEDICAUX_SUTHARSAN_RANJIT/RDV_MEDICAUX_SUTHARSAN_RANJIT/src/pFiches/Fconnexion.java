    private void jMDPpatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMDPpatActionPerformed
        
        String social = tfMDPpat.getText();
        //JOptionPane.showMessageDialog(null, "Saisissez le numéro social associé à votre compte et l'identifiant de votre compte", "Message d'information", JOptionPane.INFORMATION_MESSAGE);
        
        if(tfId.getText().length()==0 || social.length()==0){   //si il ne rentre pas tous les champs on affiche un msg
            JOptionPane.showMessageDialog(null, "Saisissez l'identifiant et le numero de telephone", "Message d'avertissement", JOptionPane.ERROR_MESSAGE);
        }
        if(tfId.getText().length()!=0 && social.length()!=0){
            if(User.recupererMDPpat(tfId.getText(), Integer.parseInt(social))==1){  //si il arrive à bien procéder alors on poursuit

                    JOptionPane.showMessageDialog(null, "Vous avez reussi vous connecter", "Message d'information", JOptionPane.INFORMATION_MESSAGE);
                    fichselection = new Fselection(this, false, 1,User);
                    this.setVisible(false);
                    fichselection.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Vous n'avez pas reussi vous connecter", "Message d'erreur", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jMDPpatActionPerformed

