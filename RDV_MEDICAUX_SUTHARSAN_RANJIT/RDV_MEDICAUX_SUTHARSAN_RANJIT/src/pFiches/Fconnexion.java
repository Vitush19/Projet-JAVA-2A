    private void jContinuerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jContinuerActionPerformed
        int indicPraticien = 2;
        int reussite = 2;
        
        
        if (rbPraticien.isSelected()){
            indicPraticien=0;
            reussite = User.connexion(indicPraticien, tfId.getText(), pfMotdepasse.getText());
            if(reussite == 1){  //si l'utilisateur est un patient et qu'il parvient à se connecter on poursuit en affichant la fenetre suivante
                fichselection = new Fselection(this, false, indicPraticien,User);
                this.setVisible(false);
                fichselection.setVisible(true);
            }
        }
        else if (rbPatient.isSelected()){
            indicPraticien=1;
            reussite = User.connexion(indicPraticien, tfId.getText(), pfMotdepasse.getText());
            if(reussite == 1){  //si l'utilisateur est un patient et qu'il parvient à se connecter on poursuit en affichant la fenetre suivante
                fichselection = new Fselection(this, false, indicPraticien,User);
                this.setVisible(false);
                fichselection.setVisible(true);
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Veuillez selectionner patient ou praticien !");
 
    }//GEN-LAST:event_jContinuerActionPerformed

